public class Calcul {

	/**
	 * @param a 1er nombre
	 * @param b 2eme nombre
	 * @return la somme de 2 nombres
	 */
	public int somme(int a, int b){
		return a+b;
	}
	
	/**
	 * @param a 1er nombre
	 * @param b 2eme nombre
	 * @return la soustraction de 2 nombre
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
